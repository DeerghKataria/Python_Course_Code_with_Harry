# This code is used to see the type of variable assigned using 'input()' function
# Prefer doing such codes directly on the terminal.

a = input("Enter Name: ")
print(type(a))  # ---> class 'str'

b = input("Enter the Number: ")
print(type(b))  # ---> class 'str'

# Converting 
b = int(b)
print(type(b))  # ---> class 'int'

